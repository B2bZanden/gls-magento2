namespace TIG\GLS\Setup;

use Magento\Framework\DB\Ddl\Table;
use Magento\Framework\Setup\ModuleContextInterface;
use Magento\Framework\Setup\SchemaSetupInterface;
use Magento\Framework\Setup\UpgradeSchemaInterface;

/**
 * Class UpgradeSchema
 * @package TIG\GLS\Setup
 */
class UpgradeSchema implements UpgradeSchemaInterface
{
    const GLS_PARCEL_QUANTITY       = 'gls_parcel_quantity';
    const GLS_PARCEL_QUANTITY_LABEL = 'GLS Parcel Quantity';

    /**
     * @param SchemaSetupInterface   $setup
     * @param ModuleContextInterface $context
     */
    public function upgrade(SchemaSetupInterface $setup, ModuleContextInterface $context)
    {
        $installer = $setup;

        $installer->startSetup();

        if (version_compare($context->getVersion(), '1.3.1', '<')) {
            $installer->getConnection()->addColumn(
                $installer->getTable('sales_order'),
                self::GLS_PARCEL_QUANTITY,
                [
                    'type'     => Table::TYPE_INTEGER,
                    'nullable' => true,
                    'default'  => null,
                    'comment'  => self::GLS_PARCEL_QUANTITY_LABEL,
                    'after'    => InstallSchema::GLS_DELIVERY_OPTION
                ]
            );
        }

        $installer->endSetup();
    }
}
